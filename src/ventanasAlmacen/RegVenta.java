    private void jButton_RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_RegistrarActionPerformed
        
        
        String codigoTransaccion, fechaVenta, iva, subtotal, total, cantidadVendida, cliente, producto;

        codigoTransaccion = jTextField_CodigoTransaccion.getText().trim();
        fechaVenta = jTextField_FechaVenta.getText().trim();
        iva = jTextField_Iva.getText().trim();
        subtotal = jTextField_Subtotal.getText().trim();
        total = jTextField_Total.getText().trim();
        cantidadVendida = jTextField_CantidadVendida.getText().trim();
        cliente = jTextField_Cliente.getText().trim();
        producto = jTextField_Producto.getText().trim();
        
        

        Object[] fila = new Object[8];

            fila[0] = codigoTransaccion;
            fila[1] = fechaVenta;
            fila[2] = cliente;
            fila[3] = producto;
            fila[4] = cantidadVendida;
            fila[5] = subtotal;
            fila[6] = iva;
            fila[7] = total;
        
        model.addRow(fila);
       
        Limpiar();
    }//GEN-LAST:event_jButton_RegistrarActionPerformed


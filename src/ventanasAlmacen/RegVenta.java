    private void jButton_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ActualizarActionPerformed
      
        int selectedRow = jTable_Venta.getSelectedRow(); 
    if (selectedRow >= 0) {
        
        String codigoTransaccion, fechaVenta, iva, subtotal, total, cantidadVendida, cliente, producto;

        codigoTransaccion = jTextField_CodigoTransaccion.getText().trim();
        fechaVenta = jTextField_FechaVenta.getText().trim();
        iva = jTextField_Iva.getText().trim();
        subtotal = jTextField_Subtotal.getText().trim();
        total = jTextField_Total.getText().trim();
        cantidadVendida = jTextField_CantidadVendida.getText().trim();
        cliente = jTextField_Cliente.getText().trim();
        producto = jTextField_Producto.getText().trim();

        // Actualizar los datos en la fila seleccionada
        model.setValueAt(codigoTransaccion, selectedRow, 0);
        model.setValueAt(fechaVenta, selectedRow, 1);
        model.setValueAt(cliente, selectedRow, 2);
        model.setValueAt(producto, selectedRow, 3);
        model.setValueAt(cantidadVendida, selectedRow, 4);
        model.setValueAt(subtotal, selectedRow, 5);
        model.setValueAt(iva, selectedRow, 6);
        model.setValueAt(total, selectedRow, 7);
        
    }
        
        Limpiar();
    }//GEN-LAST:event_jButton_ActualizarActionPerformed

